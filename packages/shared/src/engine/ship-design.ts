// calculateDesignStats
// ---------------------------------------------------------------------------

/**
 * Aggregate combat and utility statistics across all components in a design.
 *
 * Speed is the maximum speed value found across all engine components.
 * Sensor range is the maximum range across all sensor components.
 * All damage, shield, armor, and repair values are summed.
 * Cost includes the hull base cost plus all component costs, modified by armour plating.
 * Effective hull points factor in armour plating.
 */
export function calculateDesignStats(
  design: ShipDesign,
  hull: HullTemplate,
  components: ShipComponent[],
): DesignStats {
  const componentById = new Map(components.map((c) => [c.id, c]));
  const ap = design.armourPlating ?? 0;

  const stats: DesignStats = {
    totalDamage: 0,
    totalShields: 0,
    totalArmor: 0,
    speed: 0,
    sensorRange: 0,
    repairRate: 0,
    cost: getEffectiveCost(hull.baseCost, ap),
    effectiveHullPoints: getEffectiveHullPoints(hull.baseHullPoints, ap),
    accuracyBonus: 0,
    evasionBonus: 0,
    powerOutput: 0,
    powerDraw: 0,
    powerBuffer: 0,
    powerBalance: 0,
    crewCount: hull.baseCrew ?? 0,
    supplyCapacity: hull.baseSupplyCapacity ?? 15,
    mass: hull.baseHullPoints * 10,
    manned: hull.manned !== false,
  };

  for (const assignment of design.components) {
    const component = componentById.get(assignment.componentId);
    if (!component) continue;

    stats.cost += component.cost;

    // Accumulate power: engines generate, everything else consumes.
    // Warp drives are excluded from combat power draw — you don't warp and fight simultaneously.
    if (component.type === 'engine') {
      stats.powerOutput += component.stats['powerOutput'] ?? 0;
    } else if (component.type !== 'warp_drive') {
      stats.powerDraw += component.stats['powerDraw'] ?? 0;
    }

    // Accumulate power buffer from batteries/capacitors
    if (component.stats['powerBuffer']) {
      stats.powerBuffer += component.stats['powerBuffer'];
    }

    switch (component.type) {
      case 'weapon_beam':
      case 'weapon_projectile':
      case 'weapon_missile':
      case 'weapon_point_defense':
        stats.totalDamage += component.stats['damage'] ?? 0;
        break;

      case 'fighter_bay':
        stats.totalDamage +=
          (component.stats['fighterCount'] ?? 0) * (component.stats['damage'] ?? 0);
        break;

      case 'shield':
        stats.totalShields += component.stats['shieldStrength'] ?? 0;
        break;

      case 'armor':
        stats.totalArmor += component.stats['armorRating'] ?? 0;
        break;

      case 'engine':
        stats.speed = Math.max(stats.speed, component.stats['speed'] ?? 0);
        break;

      case 'warp_drive':
        if (stats.speed === 0) {
          stats.speed = Math.max(stats.speed, component.stats['warpSpeed'] ?? 0);
        }
        break;

      case 'sensor':
      case 'advanced_sensors':
      case 'scanner':
        stats.sensorRange = Math.max(stats.sensorRange, component.stats['sensorRange'] ?? 0);
        // Scanner components may also grant a range bonus (e.g. deep_space_telescope)
        if (component.stats['sensorRangeBonus']) {
          stats.sensorRange += component.stats['sensorRangeBonus'];
        }
        break;

      case 'repair_drone':
      case 'damage_control':
        stats.repairRate += component.stats['repairRate'] ?? 0;
        break;

      case 'targeting_computer':
        stats.accuracyBonus += component.stats['accuracyBonus'] ?? 0;
        break;

      case 'ecm_suite':
        stats.evasionBonus += component.stats['evasionBonus'] ?? 0;
        break;

      case 'life_support':
      case 'special':
        break;
    }
  }

  // Accumulate mass from slot components (each component's cost * 0.1 as a proxy)
  for (const assignment of design.components) {
    const component = componentById.get(assignment.componentId);
    if (!component) continue;
    stats.mass += component.cost * 0.1;
  }

  // Accumulate armour mass (armour plating adds hull mass proportional to fraction)
  stats.mass += hull.baseHullPoints * 10 * ap * 2;

  // Core system overrides — accumulate stat bonuses from upgraded tiers
  if (design.coreSystemOverrides) {
    for (const override of design.coreSystemOverrides) {
      const comp = SHIP_COMPONENT_BY_ID[override.componentId] ?? componentById.get(override.componentId);
      if (!comp) continue;

      // All core systems may draw power
      stats.powerDraw += comp.stats['powerDraw'] ?? 0;

      // Add cost and mass contribution
      stats.cost += comp.cost;
      stats.mass += comp.cost * 0.1;

      switch (override.role) {
        case 'power_reactor':
          stats.powerOutput += comp.stats['powerOutput'] ?? 0;
          break;
        case 'rcs_thrusters':
          stats.evasionBonus += comp.stats['evasionBonus'] ?? 0;
          break;
        case 'computer_core':
          stats.accuracyBonus += comp.stats['accuracyBonus'] ?? 0;
          break;
        case 'bio_reclamation':
          stats.supplyCapacity += comp.stats['supplyBonus'] ?? 0;
          break;
        case 'main_engine':
          stats.speed = Math.max(stats.speed, comp.stats['speed'] ?? 0);
          stats.powerOutput += comp.stats['powerOutput'] ?? 0;
          break;
        default:
          // sensor_suite, life_support, temperature_control, comms_array
          // — stat bonuses handled via their component type in the slot loop above
          break;
      }
    }
  }

  // Apply hull base speed as a floor if no engine was fitted
  if (stats.speed === 0) {
    stats.speed = hull.baseSpeed;
  }

  // Round mass to nearest integer for display
  stats.mass = Math.round(stats.mass);

  // Calculate net power balance
  stats.powerBalance = stats.powerOutput + stats.powerBuffer - stats.powerDraw;

  return stats;
}

