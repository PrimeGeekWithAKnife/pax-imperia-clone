/**
 * Ship design engine — pure functions for designing, validating, and
 * instantiating ships from hull templates and components.
 *
 * All functions are side-effect free. Callers are responsible for persisting
 * any state changes returned from these functions.
 */

import type { HullTemplate, ShipComponent, ShipDesign, Ship, ComponentType, SlotPosition } from '../types/ships.js';
import { getEffectiveHullPoints, getEffectiveCost } from '../types/ships.js';
import { generateId } from '../utils/id.js';
import { TECH_AGES } from '../constants/game.js';
import { HULL_TEMPLATES, SHIP_COMPONENT_BY_ID } from '../../data/ships/index.js';

// ---------------------------------------------------------------------------
// Public types
// ---------------------------------------------------------------------------

export interface DesignStats {
  totalDamage: number;
  totalShields: number;
  totalArmor: number;
  speed: number;
  sensorRange: number;
  repairRate: number;
  cost: number;
  /** Effective hull points after armour plating. */
  effectiveHullPoints: number;
  /** Accuracy bonus from targeting computers. */
  accuracyBonus: number;
  /** Evasion bonus from ECM suites. */
  evasionBonus: number;
  /** Total power generated by engines. */
  powerOutput: number;
  /** Total power consumed by all systems. */
  powerDraw: number;
  /** Power buffer from batteries/capacitors. */
  powerBuffer: number;
  /** Net power balance (output + buffer - draw). Negative = underpowered. */
  powerBalance: number;
  /** Crew complement from hull template. */
  crewCount: number;
  /** Supply capacity in turns (hull base + bio reclamation bonus). */
  supplyCapacity: number;
  /** Estimated mass: hull mass + component mass + armour mass. */
  mass: number;
  /** Whether this design requires a crew. Unmanned hulls (probes) are false. */
  manned: boolean;
}

export interface ValidationResult {
  valid: boolean;
  errors: string[];
}

